<?php

  function addition($x, $y) {
    return $x + $y;
  }

  function subtract($x, $y) {
    return $x - $y;
  }

  function multiply($x, $y) {
    return $x * $y;
  }

  function divide($x, $y) {
    return $x / $y;
  }
  
  function root($x){
      return sqrt($x);
  }
  
